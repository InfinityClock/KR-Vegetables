// ─── HELPER FUNCTIONS ─────────────────────────────────────────────────────────
export const getCategoryById = (id) => CATEGORIES.find((c) => c.id === id)

export const getProductsByCategory = (categoryId) =>
  PRODUCTS.filter((p) => p.category_id === categoryId && p.is_active !== false)

export const getFeaturedProducts = () =>
  PRODUCTS.filter((p) => p.is_featured)

export const getOfferProducts = () =>
  PRODUCTS.filter((p) => p.offer_price && p.offer_price < p.price)

export const searchProducts = (query) => {
  const q = query.toLowerCase().trim()
  if (!q) return PRODUCTS
  return PRODUCTS.filter(
    (p) =>
      p.name.toLowerCase().includes(q) ||
      p.description?.toLowerCase().includes(q) ||
      getCategoryById(p.category_id)?.name.toLowerCase().includes(q)
  )
}

// Enrich product with category info
export const enrichProduct = (product) => ({
  ...product,
  categories: getCategoryById(product.category_id),
})

export const ENRICHED_PRODUCTS = PRODUCTS.map(enrichProduct)
